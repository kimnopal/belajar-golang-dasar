package main

import "fmt"

func main() {
	var name string

	name = "Naufal"
	fmt.Println(name)

	name = "Hakim"
	fmt.Println(name)

	var friendName = "Budi"
	fmt.Println(friendName)

	var age = 20
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	var (
		firstName = "Naufal"
		lastName  = "Hakim"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
